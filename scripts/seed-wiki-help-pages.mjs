async function main() {
  let inserted = 0
  let skipped = 0

  for (const p of SEED_PAGES) {
    const exists = await pool.query(
      `SELECT id FROM wiki_pages WHERE title = $1 AND is_deleted IS NOT TRUE`,
      [p.title]
    )

    if (exists.rowCount > 0) {
      console.log(`SKIP   ${p.title} (already exists)`)
      skipped++
      continue
    }

    const insertRes = await pool.query(
      `INSERT INTO wiki_pages (
        title, slug, namespace, content, summary, categories, tags,
        creator, last_editor, last_edit_date, last_edit_summary, current_revision,
        edits, is_stub, is_featured
      ) VALUES ($1, $2, $3, $4, $5, $6, $7, 'system', 'system', NOW(),
        '도움말 시드', 1, 1, false, false)
      RETURNING id`,
      [
        p.title,
        p.slug,
        p.namespace,
        p.content,
        p.summary,
        ['이랑위키', '도움말'],
        ['help'],
      ]
    )
    const pageId = insertRes.rows[0].id

    await pool.query(
      `INSERT INTO wiki_revisions (
        page_id, revision_number, content, summary, author,
        edit_type, is_minor_edit, is_automated, content_length, size_change, timestamp_at, created_at, updated_at
      ) VALUES ($1, 1, $2, '도움말 시드 (system)', 'system',
        'create', false, true, $3, $3, NOW(), NOW(), NOW())`,
      [pageId, p.content, p.content.length]
    )

    console.log(`CREATE ${p.title} (${p.content.length} bytes)`)
    inserted++
  }

  console.log(`\n${inserted} inserted, ${skipped} skipped\n`)
  await pool.end()
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
